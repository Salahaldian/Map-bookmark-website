import React, { useEffect } from 'react';

export default function Logout() {

  useEffect(() => {
    // يمكنك تنفيذ أي عمليات تسجيل الخروج اللازمة هنا
    // مثلاً: إزالة بيانات الجلسة أو تنظيف التخزين المحلي
    localStorage.clear(); // يمسح جميع البيانات في التخزين المحلي

    // توجيه المستخدم إلى الصفحة الرئيسية بعد تسجيل الخروج
    window.location.href = '/'; // توجيه المستخدم إلى صفحة اللوحة بعد تسجيل الدخول الناجح
}, []);

  return (
    <div>
      <h2>Logging out...</h2>
      {/* يمكنك إضافة رسالة أخرى أو تصميم إضافي هنا إذا رغبت */}
    </div>
  );
}